package main

import "fmt"

func main() {
	fmt.Println("Arya")
	fmt.Println("Arya Duta")
	fmt.Println("Arya Duta Perdana")

	fmt.Println(len("Arya")) // Jumlah karakter string
	fmt.Println("Arya Duta"[0]) // Ambil karakter pada posisi yg ditentukan. Output dalam byte
	fmt.Println("Arya Duta Perdana"[1]) // Output dalam byte
}